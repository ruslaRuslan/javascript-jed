function prop2Taker(obj) {
    if ('prop-2' in obj) {
        return obj['prop-2'];
    }
}

console.log(prop2Taker({ one: 1, 'prop-2': 2 }));  // 2
console.log(prop2Taker({ 'prop-2': 'two', prop: 'test' }));
